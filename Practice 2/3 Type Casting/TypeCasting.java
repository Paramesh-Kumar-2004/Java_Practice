
class TypeCasting{
    public static void main(String[] args){

        int VP = 65;
        System.out.println("The Number Is "+VP+" And The Char (Type Casting) : "+(char)VP);
        
        System.out.print("Type Casting : ");
        for(int i=65; i<=90; i++){
            System.out.print((char)i +" ");
        }

    }
}